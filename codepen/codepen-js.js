const App = () => {
  const [currentSlide, setCurrentSlide] = React.useState(0);
  const [isPlaying, setIsPlaying] = React.useState(false);
  const [progress, setProgress] = React.useState(0);
  const isMobile = MaterialUI.useMediaQuery('(max-width:768px)');
  const isTablet = MaterialUI.useMediaQuery('(max-width:1024px)');

  const currentData = queerHistoryData[currentSlide];

  const theme = MaterialUI.createTheme({
  palette: {
    mode: 'dark',
    primary: {
      main: currentData.theme.color,
    },
    secondary: {
      main: currentData.theme.color,
    },
      background: {
        default: '#0a0a0a',
        paper: 'rgba(0, 0, 0, 0.6)',
      },
      text: {
        primary: '#ffffff',
        secondary: 'rgba(255, 255, 255, 0.85)',
      },
    },
    typography: {
      fontFamily: '"Roboto", "Helvetica", "Arial", sans-serif',
      h1: {
        fontSize: isMobile ? '2rem' : '3rem',
        fontWeight: 700,
        marginBottom: '1rem',
      },
      h2: {
        fontSize: isMobile ? '1.5rem' : '2rem',
        fontWeight: 500,
        marginBottom: '0.5rem',
      },
      body1: {
        fontSize: isMobile ? '1rem' : '1.1rem',
        lineHeight: 1.6,
      },
    },
    components: {
      MuiCard: {
        styleOverrides: {
          root: {
            backdropFilter: 'blur(20px)',
            border: '1px solid rgba(255, 255, 255, 0.1)',
          },
        },
      },
    },
  });

  const [backgroundStyle, setBackgroundStyle] = React.useState(currentData.theme.background);
  
  React.useEffect(() => {
    setBackgroundStyle(currentData.theme.background);
  }, [currentData.theme.background]);

  React.useEffect(() => {
    let interval;
    if (isPlaying) {
      interval = setInterval(() => {
        setProgress((prev) => {
          if (prev >= 100) {
            nextSlide();
            return 0;
          }
          return prev + 1;
        });
      }, 100);
    } else {
      setProgress(0);
    }
    return () => clearInterval(interval);
  }, [isPlaying, currentSlide]);

  const nextSlide = () => {
    setCurrentSlide((prev) => (prev + 1) % queerHistoryData.length);
    setProgress(0);
  };

  const prevSlide = () => {
    setCurrentSlide((prev) => (prev - 1 + queerHistoryData.length) % queerHistoryData.length);
    setProgress(0);
  };

  const goToSlide = (index) => {
    setCurrentSlide(index);
    setProgress(0);
    setIsPlaying(false);
  };

  const togglePlayPause = () => {
    setIsPlaying(!isPlaying);
  };

  const resetSlideshow = () => {
    setCurrentSlide(0);
    setProgress(0);
    setIsPlaying(false);
  };

  return React.createElement(MaterialUI.ThemeProvider, { theme },
    React.createElement(MaterialUI.CssBaseline),
    React.createElement('div', {
      style: {
        background: backgroundStyle,
        minHeight: '100vh',
        position: 'relative',
        overflow: 'hidden',
        transition: 'background 1s ease-in-out',
      }
    },
      React.createElement(MaterialUI.Box, {
        sx: {
          position: 'fixed',
          top: 0,
          left: 0,
          right: 0,
          zIndex: 1000,
          backgroundColor: 'rgba(0, 0, 0, 0.8)',
          backdropFilter: 'blur(10px)',
          borderBottom: '1px solid rgba(255, 255, 255, 0.1)',
        }
      },
        React.createElement(MaterialUI.Container, { maxWidth: 'lg' },
          React.createElement(MaterialUI.Box, {
            sx: {
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'space-between',
              padding: '1rem 0',
              flexWrap: 'wrap',
              gap: 2,
            }
          },
            React.createElement(MaterialUI.Typography, {
              variant: 'h6',
              sx: { fontWeight: 600 }
            }, '🏳️‍🌈 Queer History: A Journey Through Time'),
            
            // Navigation Controls
            React.createElement(MaterialUI.Box, {
              sx: { display: 'flex', alignItems: 'center', gap: 1 }
            },
              React.createElement(MaterialUI.IconButton, {
                onClick: prevSlide,
                disabled: currentSlide === 0,
                color: 'primary',
                sx: {
                  backgroundColor: 'rgba(255, 255, 255, 0.1)',
                  '&:hover': { backgroundColor: 'rgba(255, 255, 255, 0.2)' },
                }
              }, '‹'),
              
              React.createElement(MaterialUI.IconButton, {
                onClick: resetSlideshow,
                color: 'primary'
              }, '↻'),
              
              React.createElement(MaterialUI.IconButton, {
                onClick: togglePlayPause,
                color: 'primary'
              }, isPlaying ? '⏸' : '▶'),
              
              React.createElement(MaterialUI.IconButton, {
                onClick: nextSlide,
                disabled: currentSlide === queerHistoryData.length - 1,
                color: 'primary',
                sx: {
                  backgroundColor: 'rgba(255, 255, 255, 0.1)',
                  '&:hover': { backgroundColor: 'rgba(255, 255, 255, 0.2)' },
                }
              }, '›'),
              
              React.createElement(MaterialUI.Typography, {
                variant: 'body2',
                sx: { ml: 1 }
              }, `${currentSlide + 1} / ${queerHistoryData.length}`)
            )
          ),
          isPlaying && React.createElement(MaterialUI.LinearProgress, {
            variant: 'determinate',
            value: progress,
            sx: {
              height: 3,
              backgroundColor: 'rgba(255, 255, 255, 0.1)',
              '& .MuiLinearProgress-bar': {
                backgroundColor: theme.palette.primary.main,
              },
            }
          })
        )
      ),

      React.createElement(MaterialUI.Container, {
        maxWidth: 'lg',
        sx: { pt: 12, pb: 4 }
      },
        React.createElement(MaterialUI.Fade, {
          in: true,
          timeout: 800,
          key: currentSlide
        },
          React.createElement(MaterialUI.Card, {
            sx: {
              maxWidth: '100%',
              margin: '0 auto',
              backgroundColor: 'rgba(0, 0, 0, 0.7)',
              backdropFilter: 'blur(20px)',
              border: '1px solid rgba(255, 255, 255, 0.2)',
              overflow: 'visible',
              position: 'relative',
            }
          },
            React.createElement(MaterialUI.Chip, {
              label: currentData.year,
              sx: {
                position: 'absolute',
                top: -12,
                right: 20,
                backgroundColor: theme.palette.primary.main,
                color: 'white',
                fontWeight: 600,
                fontSize: '1rem',
                zIndex: 1,
              }
            }),

            React.createElement(MaterialUI.CardMedia, {
              component: 'img',
              height: isMobile ? 200 : isTablet ? 300 : 400,
              image: currentData.image,
              alt: currentData.title,
              sx: {
                objectFit: 'cover',
                filter: 'brightness(0.4) contrast(1.1)',
              }
            }),

            React.createElement(MaterialUI.CardContent, {
              sx: { padding: isMobile ? 2 : 4 }
            },
              React.createElement(MaterialUI.Box, {
                sx: { display: 'flex', alignItems: 'center', mb: 2, flexWrap: 'wrap' }
              },
                React.createElement(MaterialUI.Typography, {
                  variant: 'h1',
                  sx: { mr: 2, fontSize: isMobile ? '2rem' : '3rem' }
                }, currentData.icon),
                React.createElement(MaterialUI.Box, {},
                  React.createElement(MaterialUI.Typography, {
                    variant: 'h1',
                    color: 'primary',
                    sx: {
                      textShadow: '2px 2px 4px rgba(0, 0, 0, 0.9)',
                      fontWeight: 700
                    }
                  }, currentData.title),
                  React.createElement(MaterialUI.Typography, {
                    variant: 'h2',
                    sx: {
                      color: 'rgba(255, 255, 255, 0.9)',
                      textShadow: '1px 1px 3px rgba(0, 0, 0, 0.8)',
                      fontWeight: 500
                    }
                  }, currentData.subtitle)
                )
              ),

              React.createElement(MaterialUI.Typography, {
                variant: 'body1',
                sx: { 
                  mb: 3, 
                  textAlign: 'justify',
                  color: '#ffffff',
                  fontWeight: 400,
                  lineHeight: 1.7,
                  textShadow: '1px 1px 2px rgba(0, 0, 0, 0.8)'
                }
              }, currentData.description),

              React.createElement(MaterialUI.Divider, {
                sx: { my: 3, borderColor: 'rgba(255, 255, 255, 0.1)' }
              }),

              React.createElement(MaterialUI.Paper, {
                sx: { 
                  p: 2, 
                  mb: 3, 
                  backgroundColor: 'rgba(0, 0, 0, 0.4)',
                  backdropFilter: 'blur(10px)',
                  border: '1px solid rgba(255, 255, 255, 0.1)'
                }
              },
                React.createElement(MaterialUI.Typography, {
                  variant: 'h6',
                  sx: { 
                    mb: 2, 
                    color: 'primary.main',
                    fontWeight: 600,
                    textShadow: '1px 1px 2px rgba(0, 0, 0, 0.8)'
                  }
                }, 'Key Figures',
                  React.createElement(MaterialUI.Typography, {
                    variant: 'caption',
                    sx: { 
                      display: 'block', 
                      color: 'rgba(255, 255, 255, 0.7)',
                      fontSize: '0.75rem',
                      fontWeight: 400,
                      mt: 0.5
                    }
                  }, 'Click to learn more on Wikipedia')
                ),
                React.createElement(MaterialUI.Box, {
                  sx: { display: 'flex', flexWrap: 'wrap', gap: 1 }
                },
                  ...currentData.keyFigures.map((figure, index) =>
                    React.createElement(MaterialUI.Chip, {
                      key: index,
                      label: figure,
                      variant: 'outlined',
                      clickable: true,
                      onClick: () => {
                        let wikipediaPath;
                        
                        if (figure === "Lambda Legal") {
                          wikipediaPath = "Lambda_Legal";
                        } else if (figure === "Gert Kasteel") {
                          // Redirect to Dutch LGBT organization since individual doesn't have page
                          wikipediaPath = "COC_Nederland";
                        } else if (figure === "Tyron Garner") {
                          // Redirect to Lawrence v. Texas case since individual doesn't have page
                          wikipediaPath = "Lawrence_v._Texas";
                        } else if (figure === "Terry Miller") {
                          // Redirect to It Gets Better Project since individual doesn't have page  
                          wikipediaPath = "It_Gets_Better_Project";
                        } else if (figure === "Beto de Jesus") {
                          // Redirect to IDAHOBIT since individual doesn't have page
                          wikipediaPath = "International_Day_Against_Homophobia,_Transphobia_and_Biphobia";
                        } else {
                          // Default case - use the figure name
                          wikipediaPath = encodeURIComponent(figure.replace(/\s+/g, '_'));
                        }
                        
                        const wikipediaUrl = `https://en.wikipedia.org/wiki/${wikipediaPath}`;
                        window.open(wikipediaUrl, '_blank', 'noopener,noreferrer');
                      },
                      sx: { 
                        borderColor: 'primary.main', 
                        color: '#ffffff',
                        backgroundColor: 'rgba(0, 0, 0, 0.3)',
                        fontWeight: 500,
                        cursor: 'pointer',
                        transition: 'all 0.3s ease',
                        '&:hover': {
                          backgroundColor: 'rgba(255, 255, 255, 0.1)',
                          borderColor: 'secondary.main',
                          transform: 'translateY(-2px)',
                        }
                      }
                    })
                  )
                )
              ),

              React.createElement(MaterialUI.Paper, {
                sx: { 
                  p: 2, 
                  mb: 3, 
                  backgroundColor: 'rgba(0, 0, 0, 0.4)',
                  backdropFilter: 'blur(10px)',
                  border: '1px solid rgba(255, 255, 255, 0.1)'
                }
              },
                React.createElement(MaterialUI.Typography, {
                  variant: 'h6',
                  sx: { 
                    mb: 1, 
                    color: 'secondary.main',
                    fontWeight: 600,
                  }
                }, 'Historical Impact'),
                React.createElement(MaterialUI.Typography, {
                  variant: 'body2',
                  sx: { 
                    fontStyle: 'italic',
                    color: '#ffffff',
                    fontWeight: 400,
                  }
                }, currentData.impact)
              ),

              React.createElement(MaterialUI.Paper, {
                sx: { 
                  p: 2, 
                  backgroundColor: 'rgba(0, 0, 0, 0.4)',
                  backdropFilter: 'blur(10px)',
                  border: '1px solid rgba(255, 255, 255, 0.1)'
                }
              },
                React.createElement(MaterialUI.Typography, {
                  variant: 'h6',
                  sx: { 
                    mb: 2,
                    color: '#ffffff',
                    fontWeight: 600,
                  }
                }, 'Sources & Further Reading'),
                React.createElement(MaterialUI.List, { dense: true },
                  ...currentData.sources.map((source, index) =>
                    React.createElement(MaterialUI.ListItem, {
                      key: index,
                      sx: { py: 0.5 }
                    },
                      React.createElement(MaterialUI.ListItemText, {
                        primary: source,
                        sx: {
                          '& .MuiListItemText-primary': {
                            fontSize: '0.9rem',
                            color: 'rgba(255, 255, 255, 0.9)',
                            fontWeight: 400,
                          },
                        }
                      })
                    )
                  )
                )
              )
            )
          )
        ),

        React.createElement(MaterialUI.Box, {
          sx: {
            display: 'flex',
            justifyContent: 'center',
            alignItems: 'center',
            mt: 4,
            gap: 1,
          }
        },
          ...queerHistoryData.map((_, index) =>
            React.createElement(MaterialUI.Box, {
              key: index,
              onClick: () => goToSlide(index),
              sx: {
                width: 12,
                height: 12,
                borderRadius: '50%',
                backgroundColor:
                  index === currentSlide
                    ? theme.palette.primary.main
                    : 'rgba(255, 255, 255, 0.3)',
                cursor: 'pointer',
                transition: 'all 0.3s ease',
                '&:hover': {
                  backgroundColor: theme.palette.primary.light,
                  transform: 'scale(1.2)',
                },
              }
            })
          )
        ),

        React.createElement(MaterialUI.Box, {
          sx: { textAlign: 'center', mt: 6, mb: 2 }
        },
          React.createElement(MaterialUI.Typography, {
            variant: 'body2',
            color: 'text.secondary'
          }, 'Created with ❤️ for the DEV.to Frontend Challenge: June Celebrations'),
          React.createElement(MaterialUI.Typography, {
            variant: 'body2',
            color: 'text.secondary',
            sx: { mt: 1 }
          }, 'Celebrating LGBTQ+ history and the ongoing fight for equality')
        )
      )
    )
  );
};

const root = ReactDOM.createRoot(document.getElementById('root'));
root.render(React.createElement(App));
